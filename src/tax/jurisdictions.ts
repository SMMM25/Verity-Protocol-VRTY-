/**
 * Get all supported jurisdictions
 */
export function getSupportedJurisdictions(): Array<{ code: string; name: string; region: string }> {
  return Array.from(JURISDICTION_RULES.values()).map((j) => ({
    code: j.code,
    name: j.name,
    region: j.region,
  }));
}

/**
 * Get jurisdiction by code
 */
export function getJurisdictionByCode(code: string): JurisdictionRules | undefined {
  return JURISDICTION_RULES.get(code);
}

/**
 * Get jurisdictions by region
 */
export function getJurisdictionsByRegion(region: string): JurisdictionRules[] {
  return Array.from(JURISDICTION_RULES.values()).filter((j) => j.region === region);
}

/**
 * Get tax-friendly jurisdictions (0% capital gains)
 */
export function getTaxFriendlyJurisdictions(): JurisdictionRules[] {
  return Array.from(JURISDICTION_RULES.values()).filter(
    (j) => j.shortTermRate === 0 && j.longTermRate === 0
  );
}

/**
 * Get jurisdictions with holding period exemptions
 */
export function getHoldingPeriodExemptionJurisdictions(): JurisdictionRules[] {
  return Array.from(JURISDICTION_RULES.values()).filter(
    (j) => j.cryptoSpecificRules?.holdingPeriodExemption
  );
}

/**
 * Get jurisdictions with treaty partners
 */
export function getJurisdictionsWithTreaty(countryCode: string): JurisdictionRules[] {
  return Array.from(JURISDICTION_RULES.values()).filter((j) =>
    j.treatyPartners.includes(countryCode)
  );
}

export default JURISDICTION_RULES;
